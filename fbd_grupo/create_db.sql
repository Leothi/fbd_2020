        ID_VIAGEM,
        ANTIGO,
        NOVO,
        TIMESTAMP
    )
    VALUES(
        NEW.ID_VIAGEM,
        JSON_OBJECT(
            "data_inicio", OLD.DATA_INICIO,
            "data_FIM", OLD.DATA_FIM,
            "ministerio", OLD.ID_MINISTERIO,
            "cpf", OLD.ID_PESSOA_CARGO_FUNCAO
        ),
        JSON_OBJECT(
            "data_inicio", NEW.DATA_INICIO,
            "data_FIM", NEW.DATA_FIM,
            "ministerio", NEW.ID_MINISTERIO,
            "cpf", NEW.ID_PESSOA_CARGO_FUNCAO
        ),
        CURRENT_DATE
    );
END$$


