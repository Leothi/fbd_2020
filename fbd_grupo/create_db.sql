DELIMITER $$
USE `VIAGENS_DB`$$
CREATE DEFINER = CURRENT_USER TRIGGER `VIAGENS_DB`.`LOG_VIAGEM_AFTER_INSERT` AFTER INSERT ON `VIAGEM` FOR EACH ROW
BEGIN
  INSERT INTO VIAGENS_DB.LOG_VIAGEM (
        ID_VIAGEM,
        ANTIGO,
        NOVO,
        TIMESTAMP
    )
    VALUES(
        NEW.ID_VIAGEM,
        null,
        JSON_OBJECT(
            "data_inicio", NEW.DATA_INICIO,
            "data_FIM", NEW.DATA_FIM,
            "ministerio", NEW.ID_MINISTERIO,
            "cpf", NEW.ID_PESSOA_CARGO_FUNCAO
        ),
        CURRENT_DATE
    );
END$$

USE `VIAGENS_DB`$$
